package gameEngine;
import java.awt.*;

public class Rect {
    public int x,y,width,height;
    public char texture;

    public Rect(int x, int y, int width, int height, char texture){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.texture = texture;
    }

}

